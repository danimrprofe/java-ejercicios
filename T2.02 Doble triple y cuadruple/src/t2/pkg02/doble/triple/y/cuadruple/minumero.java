package t2.pkg02.doble.triple.y.cuadruple;

/**
 *
 * @author dnick
 */
public class minumero {
 
    private final int num;
    public minumero(int numero)
    {
        num = numero;
    }
    public int doble(){
        return num*2;
    }
    public int triple(){
        return num*3;
    }
    public int cuadruple(){
        return num*4;
    }
}
